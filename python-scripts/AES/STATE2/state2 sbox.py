# Inverse S-Box (for decryption)
inverse_aes_sbox = {v: k for k, v in aes_sbox.items()}

def substitute_byte(byte_str, sbox_dict=aes_sbox):
    """Substitute a byte using the specified S-Box"""
    return sbox_dict.get(byte_str, byte_str)

def substitute_bytes(byte_array, sbox_dict=aes_sbox):
    """Substitute an array of bytes"""
    return [substitute_byte(b, sbox_dict) for b in byte_array]

def print_sbox(sbox_dict, title="S-Box"):
    """Print the S-Box in a readable format"""
    print(f"\n{title} (16x16):")
    print("   " + " ".join(f"{i:02X}" for i in range(16)))
    for row in range(16):
        print(f"{row:02X} ", end="")
        for col in range(16):
            val = sbox_dict.get(f'0x{row*16+col:02X}', '??')
            print(val[-2:], end=" ")
        print()

